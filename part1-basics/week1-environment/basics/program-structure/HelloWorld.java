/**
 * 這是一個基本的 Java 程式範例
 * 展示了 Java 程式的基本結構
 */
public class HelloWorld {
    // main 方法是程式的進入點
    public static void main(String[] args) {
        // 印出 "Hello, World!" 到控制台
        System.out.println("Hello, World!");
        
        // 示範基本變數使用
        String message = "Welcome to Java!";
        System.out.println(message);
        
        // 示範簡單的數值運算
        int number = 42;
        System.out.println("The number is: " + number);
    }
}
